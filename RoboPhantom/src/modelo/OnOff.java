package modelo;

public class OnOff {
    private String usuario;
    private String id_disp;
    private String ip_disp;
    private String nome;
    private String estado;

    /**
     * @return the usuario
     */
    public String getUsuario() {
        return usuario;
    }

    /**
     * @param usuario the usuario to set
     */
    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    /**
     * @return the id_disp
     */
    public String getId_disp() {
        return id_disp;
    }

    /**
     * @param id_disp the id_disp to set
     */
    public void setId_disp(String id_disp) {
        this.id_disp = id_disp;
    }

    /**
     * @return the ip_disp
     */
    public String getIp_disp() {
        return ip_disp;
    }

    /**
     * @param ip_disp the ip_disp to set
     */
    public void setIp_disp(String ip_disp) {
        this.ip_disp = ip_disp;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the estado
     */
    public String getEstado() {
        return estado;
    }

    /**
     * @param estado the estado to set
     */
    public void setEstado(String estado) {
        this.estado = estado;
    }

   
}
